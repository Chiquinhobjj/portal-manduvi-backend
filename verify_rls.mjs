/**
 * TESTE 2: Tentativa de Escrita N√£o Autorizada
 * Verifica se usu√°rios an√¥nimos n√£o conseguem inserir dados
 */
async function testUnauthorizedWrite() {
  console.log('\nüîç TESTE 2: Tentativa de Escrita N√£o Autorizada');
  console.log('-'.repeat(50));
  
  try {
    console.log('üîç Tentando inserir dados com chave an√¥nima...');
    
    const { data, error } = await supabaseAnon
      .from('content_items')
      .insert({
        org_id: '00000000-0000-0000-0000-000000000001',
        type: 'post',
        slug: 'teste-rls-escrita',
        title: 'Teste RLS - Dados Falsos',
        excerpt: 'Tentativa de inser√ß√£o n√£o autorizada',
        body: 'Este conte√∫do n√£o deveria ser inserido',
        is_public: true,
        published_at: new Date().toISOString()
      })
      .select();

    // Se chegou at√© aqui, a inser√ß√£o foi bem-sucedida (FALHA do teste)
    if (data && data.length > 0) {
      console.log('‚ùå Teste de Escrita N√£o Autorizada: FALHA');
      console.log('   ‚úó Usu√°rio an√¥nimo conseguiu inserir dados!');
      console.log('   ‚úó Dados inseridos:', data);
      return false;
    }

    // Se houve erro, verifica se √© o erro esperado de RLS
    if (error) {
      console.log('üìù Erro capturado:', error.message);
      
      // Verifica se √© um erro de RLS
      const isRLSError = error.message.includes('new row violates row-level security policy') ||
                        error.message.includes('RLS') ||
                        error.message.includes('row-level security') ||
                        error.message.includes('permission denied');
      
      if (isRLSError) {
        console.log('‚úÖ Teste de Escrita N√£o Autorizada: SUCESSO');
        console.log('   ‚úì Pol√≠tica RLS bloqueou inser√ß√£o n√£o autorizada');
        console.log('   ‚úì Erro de seguran√ßa capturado corretamente');
        return true;
      } else {
        console.log('‚ùå Teste de Escrita N√£o Autorizada: FALHA');
        console.log('   ‚úó Erro inesperado:', error.message);
        return false;
      }
    }

    // Se n√£o houve erro nem dados, algo estranho aconteceu
    console.log('‚ùå Teste de Escrita N√£o Autorizada: FALHA');
    console.log('   ‚úó Comportamento inesperado - nem erro nem dados');
    return false;

  } catch (error) {
    // Captura erros de rede ou outros erros inesperados
    console.log('‚ùå Erro inesperado no Teste 2:', error.message);
    return false;
  }
}

